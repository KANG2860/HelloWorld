package com.jooBang.project.model;

public class MapVO {
	private int roomNo;
	private int roomWeekPrice;
	private String lat;
	private String lng;
	
	public int getRoomNo() {
		return roomNo;
	}
	public void setRoomNo(int roomNo) {
		this.roomNo = roomNo;
	}
	public int getRoomWeekPrice() {
		return roomWeekPrice;
	}
	public void setRoomWeekPrice(int roomWeekPrice) {
		this.roomWeekPrice = roomWeekPrice;
	}
	public String getLat() {
		return lat;
	}
	public void setLat(String lat) {
		this.lat = lat;
	}
	public String getLng() {
		return lng;
	}
	public void setLng(String lng) {
		this.lng = lng;
	}
	

}
